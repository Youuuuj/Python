# 지각시간 기준 : 9시 10분 또는 그 이후
# Enumerate 함수 사용

time = [540, 560, 530, 535, 552]
for i,j in enumerate(time):
    if j >= 550:
        print("%d 번째 학생 지각"%(i+1), end='\n')
    else:
        print("%d 번째 학생 정상 출석"%(i+1), end='\n')


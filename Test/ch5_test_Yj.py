# 주어진 자연수가 홀수인지 짝수인지 판별해주는 잠수(is_odd)를 작성
# 람다와 조건부표현식 사용 is_odd함수 작성
# is_odd를 사용하여 숫자입력 홀수 짝수 여부 확인

# 12-1
def is_odd(x):
    if (lambda x : x % 2 == 0)(x) == True:
        print('짝수')
    else:
        print("홀수")

is_odd(int(input('숫자입력 : ')))

# 12-2
def is_odd2(x):
    (lambda x : print('짝수') if x % 2 == 0 else print('홀수'))(x)


is_odd2(int(input('숫자입력 : ')))


